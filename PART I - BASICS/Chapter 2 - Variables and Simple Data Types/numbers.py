# UNDERSCORES IN NUMBERS

universe_age = 14_000_000_000  # This is the same as 14000000000 but easier to read
print(universe_age)  # The value printed is 14000000000 without underscores

# MULTIPLE ASSIGNMENT

x, y, z = 0, 1, 2  # You can assign values to more than one variable using a single line
