# INTEGERS

x = 4 + 2  # You can ADD integers in Python with (+)
print(x)

x = 4 - 2  # You can SUBTRACT integers with (-)
print(x)

x = 4 * 2  # You can MULTIPLY integers with (*)
print(x)

x = 4 / 2  # You can DIVIDE integers with (/)
print(x)

x = 4 ** 2  # You can ELEVATE integers with (**), this is like 4^2
print(x)

x = (4 / 2) * 2  # You can do order of operations in one expression
print(x)

# FLOATS

x = 0.4 + 0.2  # You can ADD floats in Python with (+)
print(x)

x = 0.4 - 0.2  # You can SUBTRACT floats with (-)
print(x)

x = 0.4 * 0.2  # You can MULTIPLY floats with (*)
print(x)

x = 0.4 / 0.2  # You can DIVIDE floats with (/)
print(x)

x = 0.4 ** 0.2  # You can ELEVATE floats with (**), this is like 4^2
print(x)

x = (0.4 / 0.2) * 0.2  # You can do order of operations in one expression
print(x)

