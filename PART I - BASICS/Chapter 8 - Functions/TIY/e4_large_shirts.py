def make_shirt(size='L', message='I Love Python'):
    """Displays the details of a T-Shirt order"""
    print(f"\nHere is the summary of your T-Shirt order.")
    print(f"Size: {size}")
    print(f"Message: {message}")


make_shirt()
make_shirt('M')
make_shirt(message='I Love C')
