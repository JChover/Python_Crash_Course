# This case runs the IF block
alien_color = 'green'

if alien_color == 'green':
    points = 5
else:
    points = 10

print(f"You earned {points} points.")

# This case runs the ELSE block
alien_color = 'red'

if alien_color == 'green':
    points = 5
else:
    points = 10

print(f"You earned {points} points.")
