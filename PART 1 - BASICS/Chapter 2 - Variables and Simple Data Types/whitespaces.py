print("Python")

print("\tPython")  #The \t is called a whitespace which is a tab before the word Python

print("Languages:\nPython\nC\nJavaScript")  #The \n is called a New Line which jumps the text after \n to the next line

print("Languages:\n\tPython\n\tC\n\tJavaScript")  #You can combine both at the same time

favorite_language = 'Python '
print(favorite_language)  #This prints "Python  "

print(favorite_language.rstrip())  #This prints "Python"

#The method .rstrip() removes extra spaces at the right of a string
#The method .lstrip() removes extra spaces at the left of a string
#The method .strip() removes extra spaces at the right and left of a string
