# In Python, square brackets ([]) indicate a list.

bicycles = ['trek', 'cannondale', 'redline', 'specialized']

print(bicycles)  # This prints the complete list as its defined

print(bicycles[0])  # This prints the FIRST value of the LIST

print(bicycles[-1])  # This prints the LAST value of the LIST

print(bicycles[0].title())  # You can use METHODS to LISTS items

message = f"My first bicycle was a {bicycles[0].title()}."  # You can use individual values of a LIST
print(message)
